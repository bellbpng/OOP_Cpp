#include <iostream>

using namespace std;

int main()
{
	cout << "sizeof(short) : " << sizeof(short) << " bytes" << endl;
	cout << "sizeof(int) : " << sizeof(int) << " bytes" << endl;
	cout << "sizeof(long) : " << sizeof(long) << " bytes" << endl;
	cout << "sizeof(long long) : " << sizeof(long long) << " bytes" << endl;

	return 0;
}